public final void Mostrardatos(String valor){
            //Funcion para llenar la jtable de Usuarios desde la BD
            Myconexion cc=new Myconexion();
            Connection cn=Myconexion.getConnection();
            RefrescarTabla();
            Controlproductos.addColumn("idproducto");
            Controlproductos.addColumn("nombrep");
            Controlproductos.addColumn("cantidadp");
            Controlproductos.addColumn("preciop");
         
            this.tablaproducto.setModel(Controlproductos);
            String sql;
            if (valor.equals(""))
            {
            sql="SELECT * FROM producto";
            }
            else{
            sql="SELECT * FROM producto WHERE nombrep='"+valor+"'";
            } 

            String []datos=new String [4];
            try{
            Statement st=cn.createStatement();
            ResultSet rs=st.executeQuery(sql);
            while(rs.next()){
            datos[0]=rs.getString(1);
            datos[1]=rs.getString(2);
            datos[2]=rs.getString(3);
            datos[3]=rs.getString(4);
     

            Controlproductos.addRow(datos);
            }
            tablaproducto.setModel(Controlproductos);
            }catch(SQLException ex){
            Logger.getLogger(controlproductos.class.getName()).log(Level.SEVERE,null,ex);
            JOptionPane.showMessageDialog(null, "error "+ex);
            }
        }
