    private void insertarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_insertarActionPerformed
        // TODO add your handling code here:
        String id = (String)Controlproductos.getValueAt(tablaproducto.getSelectedRow(),0);
        int can_1 = Integer.parseInt((String)Controlproductos.getValueAt(tablaproducto.getSelectedRow(),2));
        int can_2 = Integer.parseInt(txtcan.getText());
        String total = String.valueOf(can_1-can_2);
        

        

        PreparedStatement ps;
        String query = "UPDATE `producto` SET cantidadp=?  WHERE `idproducto`=?";
        try {
            ps = Myconexion.getConnection().prepareStatement(query);
            
            ps.setString(2, id);
            ps.setString(1, total);

            
            

            ps.executeUpdate();
            JOptionPane.showMessageDialog(null, " producto Modificado");
            Limpiar();
            Mostrardatos("");

        } catch (SQLException ex) {
            Logger.getLogger(controlproductos.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "error "+ex);
        }
    }//GEN-LAST:event_insertarActionPerformed

    private void reporteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_reporteActionPerformed
        // TODO add your handling code here:
        Connection cn = Myconexion.getConnection();
        try {
          JasperReport jr = (JasperReport) JRLoader.loadObject(controlproductos.class.getResource("/Reporte/report1.jasper"));
          Map parametros = new HashMap<>();
          parametros.put("Titulo", "Reporte de Productos");
            

          JasperPrint jp = JasperFillManager.fillReport(jr, parametros, cn);
          JasperViewer jv = new JasperViewer(jp, false);
          jv.setVisible(true); 
        }  
        catch (JRException ex) {
            JOptionPane.showMessageDialog(rootPane, ex);
        }
                                               

    }//GEN-LAST:event_reporteActionPerformed

